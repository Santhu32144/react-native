// Helper functions for calculations
export const getMonthlyTotals = monthData => {
  const totalIncome = Object.values(monthData.incomes).reduce(
    (sum, amount) => sum + amount,
    0,
  );
  const totalOutgoes = Object.values(monthData.outgoes).reduce(
    (sum, amount) => sum + amount,
    0,
  );

  const expenseTotals = {};
  let totalExpenses = 0;

  for (const [category, amounts] of Object.entries(monthData.expenses)) {
    const categoryTotal = amounts.reduce((sum, amount) => sum + amount, 0);
    expenseTotals[category] = categoryTotal;
    totalExpenses += categoryTotal;
  }

  return {
    totalIncome,
    totalOutgoes,
    totalExpenses,
    expenseTotals,
    netBalance: totalIncome - totalOutgoes - totalExpenses,
  };
};
